trait DateFormatter {
  def getDateAsFormattedString(day:Int, month:Int, year:Int) : String = {
      val date = new DateTime(year, month, day, 0, 0, 0)
      val formatter = new SimpleDateFormat("dd MMM YYYY");
      formatter.format(date.toDate)
  }
}
